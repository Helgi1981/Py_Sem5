# Напишите функцию f, которая на вход принимает два числа a и b, и возводит число a 
# в целую степень b с помощью рекурсии.

# Функция не должна ничего выводить, только возвращать значение.

# Пример:
# a = 3; b = 5 -> 243 (3⁵)
# a = 2; b = 3 -> 8 

def power(a, b):
    # Базовый случай: если степень равна 0, возвращаем 1
    if b == 0:
        return 1
    # Рекурсивный случай для положительной степени
    elif b > 0:
        return a * power(a, b - 1)
    # Рекурсивный случай для отрицательной степени
    else:
        return 1 / power(a, -b)

# Примеры использования
print(power(3, 5))  # 243
print(power(2, 3))  # 8


