# Напишите рекурсивную функцию sum(a, b), возвращающую сумму двух целых неотрицательных 
# чисел. Из всех арифметических операций допускаются только +1 и -1. 
# Также нельзя использовать циклы.

# Пример:
# sum(2, 2)
# # 4

def sum(a, b):
    # Базовый случай: если b равно 0, возвращаем a
    if b == 0:
        return a
    # Рекурсивный случай: увеличиваем a на 1 и уменьшаем b на 1
    else:
        return sum(a + 1, b - 1)

# Примеры использования
print(sum(2, 2))  # 4
print(sum(5, 3))  # 8
print(sum(0, 0))  # 0


